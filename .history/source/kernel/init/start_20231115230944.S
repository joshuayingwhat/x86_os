#include "os_cfg.h"

    .text
    .extern kernel_init
    .global _start
    .extern init_main
_start:
    push %ebp
    mov  %esp,%ebp
    mov  0x8(%ebp),%eax

    push %eax
    call kernel_init

    jmp $KERNEL_SELECTOR_CS, $gdt_reload
gdt_reload:
    mov $KERNEL_SELECTOR_DS, %ax
	mov %ax, %ds
	mov %ax, %ss
	mov %ax, %es
	mov %ax, %fs
	mov %ax, %gs

    mov $(stack+KERNEL_STACK_SIZE), %esp
    jmp init_main

    .bss
.comm stack,KERNEL_STACK_SIZE

.macro
    .text
    .extern do_handler_unknow
    .global exception_handle_unknow
exception_handle_unknow:
    pusha
    push %ds
    push %es
    push %fs
    push %gs

    push %esp
    call do_handler_unknow
    add %(1*4), %esp

    pop %gs
    pop %fs
    pop %es
    pop %ds
    popa
    iret